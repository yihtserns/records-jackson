package com.github.yihtserns.records.jackson;

import com.fasterxml.jackson.annotation.JacksonAnnotationsInside;
import com.fasterxml.jackson.databind.annotation.JsonDeserialize;
import com.fasterxml.jackson.databind.annotation.JsonSerialize;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotate a Record class with this to de/serialize it from/to (unnamed) tuple (in the form of JSON array).
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
@JsonDeserialize(using = RecordsTupleDeserializer.class)
@JsonSerialize(using = RecordsTupleSerializer.class)
@JacksonAnnotationsInside
public @interface JsonTuple {
}
